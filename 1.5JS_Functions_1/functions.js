// Arrow function syntax

const square =(number) => {
    return number * number;
};
square(5);
// output: 25

// Declaration

function square (number) {
    return number * number;
};
square(5);
// output: 25

// expression/anonzmous fuction
const square = function (number){
    return number * number;
};
square (5);
// output: 25
